<p class="detail-cap-one">Problem two introduces students to the benefactor and beneficiaries of their design, which in this case, is hypothetical. Author Mary Stewart hires them to design a video that summarizes chapter one of her book, Launching the Imagination: A Comprehensive Guide to Basic Design.</p>

<p class="detail-cap-one">In chapter one, she uses fine art examples to explain the basic elements of two-dimensional design. The design elements are interconnected and difficult to discuss separately, but this reading allows for a structural way of thinking about the elements. In this problem, they learn to think about design elements from a fine art perspective but communicate them using motion design tools. </p>

<p class="detail-cap-one">With a concept map template, students use familiar tools like the dictionary and thesaurus to expand the definition of particular words in the script and develop design skills by visualizing the words with a doodle. This step develops students’ imagination and gives them a couple of concepts. Finally, with a storyboard template, students visually plan the sequence of their concepts and study my solution. </p>

<p class="detail-cap-one">We meet as a class towards the end to view each other’s work in a group critique. Some students find it very helpful to see how peers solve the same problem. One of the most valuable things about critique at this level is less about what I tell them and more about how they describe their work and clarify their intended message. </p>


</div>

