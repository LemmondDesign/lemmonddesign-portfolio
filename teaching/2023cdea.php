    <p class="detail-cap-one-hl">Impact: Research reveals that most institutions of higher education deemphasize visual thinking. </p>
      
    <p class="detail-cap-one">This Design Thinking based course is impactful because it presents open-ended design problems to a cross-section of learners from diverse academic backgrounds and shows creative motion and interactive outcomes. Courses like this are an example of design in service to higher education. </p>

    <p class="detail-cap-one">My work in this course joins the growing conversation around pedagogy. Innovators like Meredith Davis propose Design Thinking principles to address new graduates’ critical thinking, problem-solving, and creative skills gaps. </p>

    <p class="detail-cap-one">Publications like AIGA Designer 2025 note that communication modalities and technologies are complex and changing rapidly. Designers are thinking about flexible, responsive, complex, and customizable solutions to ambiguous problems and crafting for different audiences and multimodal experiences. As one can imagine, teaching motion and interaction in this environment presents unique challenges, opportunities, and outcomes. </p>

</div>

<div class="detail">

    <p class="detail-cap-one">Description: Digital Art Survey and Practice is a Block One course at the University of Tulsa that focuses on aesthetic and creative experiences. Students develop critical thinking and problem-solving skills as they learn the fundamentals of motion and interaction design using emergent software. </p>

    <p class="detail-cap-one">In the spirit of interdisciplinary collaboration, this course welcomes students from every corner of the campus, whether biomedicine, geoscience, or cyber security; most come from outside the art department from diverse academic, extracurricular, and cultural backgrounds with different expectations, software proficiencies, and pedagogical experiences. </p>

    <p class="detail-cap-one">This course presents an opportunity to design problems that flex to diverse student interests and skill levels and develops critical thinking and problem-solving skills. Furthermore, when one considers the proliferation of technologies, creative software, and communication modalities this cross-section of students use daily, most are already communication designers eager to learn our histories, methods, philosophies, and tools that shape design. </p>

    <p class="detail-cap-one">Each semester begins with an emphasis on Margaret Boden’s first level of creativity, “unfamiliar combinations of familiar ideas,” in her book, The Creative Mind.</p>

    <p class="detail-cap-one">Rubrics and problems are organized into five learning objectives. They ask measurable questions like “Was the solution delivered by the due date?” and open-ended questions like “Is the solution creatively surprising?” Then, as students gain knowledge and confidence, they can reflect on my comments in the rubric, apply new things they have learned, and resubmit solutions for regrading, giving students the comfort creativity requires.</p>

      <p class="detail-cap-one">Outcomes: I am always amazed by their talent, dedication, and creativity. Course outcomes show that students from diverse academic backgrounds can successfully solve visual problems and are eager to think like a designer. Student feedback is generally positive, and remarks mention growth in creative thinking, ideation, iteration, and communication design principles. One student summarized it nicely: “I have learned much more about the process, gaining an actual method to create designs instead of constantly winging it.” </p>


</div>

<div class="detail">

<p class="detail-cap-one-hl">Problem 1: How can a designer communicate their personality type and give a sample of their skills?</p>

<p class="detail-cap-one">Problem one introduces students to the beneficiaries of their design, which in this case are their classmates and me. Students generate their own research content by answering a mix of measurable and open-ended questions and editing the results into a short video. In addition, they learn basic motion concepts like plot structure, camera framing, and scene transition. </p>

<p class="detail-cap-one">When one considers the ever-changing web of devices, apps, and plugins, it is not surprising that software is a source of anxiety. Therefore, while students must learn and demonstrate some specific software knowledge, they are encouraged to incorporate other digital and physical forms of creativity. </p>


</div>

<div class="detail">

