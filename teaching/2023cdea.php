<p class="detail-cap-one">Problem three introduces students to interactive prototyping. Students use Figma to make interactive prototypes they can test on their phones or computer. They develop visual thinking skills by making interactive components that communicate a mood. For example, make a toggle button that moves playfully or a dropdown menu that moves shyly.</p>

<p class="detail-cap-one">Students discuss prototypes with a few groups of students that give them a cross-section of ideas and solutions for their mood and component solution. This matrix can generate inspiration, iteration, and collaboration because while each cell presents a unique problem, it shares commonalities with others and even user testing.</p>

